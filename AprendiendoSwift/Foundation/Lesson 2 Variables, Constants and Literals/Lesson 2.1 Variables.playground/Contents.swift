import UIKit

// MARK: - Variables -

// MARK: Spanish
// Las variables son espacios en memoria reservados para almacenar un valor que corresponde a un tipo de dato soportado por el lenguaje de programación. Los valores almacenados en las variables pueden cambiar durante la ejecución del codigo. La palabra reservada "var" se usa para declarar variables.

// MARK: English
// Variables are memory spaces reserved to store a value that corresponds to a data type supported by the programming language. Values stored in variables can change during code execution. The "var" keyword is used to declare variables.


// MARK: - Syntax
/*
 var variableName:DataType = value
 */

// MARK: Example

var myVar = "Hello, world!"     // Declare a var
myVar = "Hola, mundo!"          // Changed value of variable

// MARK: Multiple declaration

var numberOne = 1, myName = "Miguel"
var lastName = "Martinez", age = 24

// MARK: - Explicit and implicit declaration -
// Asignacion Explicita e Implicita

var a1:Int = 200    // Explicit decleration
var a2 = 200        // implicit decleration

var b1: String = "This is a message"
var b2 = "Este es un mensaje"

var c1: Float = 3.1416
var c2 = 3.1416

var d2: Bool = false
var d1 = true

